/**
 * Generates public/sitemap.xml and public/robots.txt at build time.
 *
 * Course and instructor URLs are pulled from the public catalog API. If the API is unreachable
 * (offline dev, CI without a running backend) the script still emits the static routes and exits
 * successfully — a stale-but-valid sitemap is better than a failed build.
 *
 * Run via `npm run generate:seo` (wired into `prebuild`).
 */
import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const CLIENT_DIR = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const PUBLIC_DIR = path.join(CLIENT_DIR, 'public');

// Vite reads .env itself; this plain Node script has to. Real env vars (CI) always win.
async function loadDotEnv() {
    let contents;
    try {
        contents = await readFile(path.join(CLIENT_DIR, '.env'), 'utf8');
    } catch {
        return;
    }
    for (const line of contents.split('\n')) {
        // The value is trimmed below, so it needs no \s* tail here — one that follows a greedy
        // (.*)? only gives the engine something to backtrack over.
        const match = /^\s*([\w.-]+)\s*=(.*)$/.exec(line);
        if (!match) continue;
        const key = match[1];
        if (process.env[key] === undefined) {
            process.env[key] = (match[2] ?? '').trim().replace(/^["']|["']$/g, '');
        }
    }
}
await loadDotEnv();

const SITE_URL = (process.env.VITE_SITE_URL || 'http://localhost:5173').replace(/\/+$/, '');
const API_URL = (process.env.VITE_API_URL || '').replace(/\/+$/, '');
const PAGE_SIZE = 100; // PaginationConstants.MaxPageSize on the backend

/** Public, indexable routes that exist regardless of the database contents. */
const STATIC_ROUTES = [
    { path: '/', changefreq: 'weekly', priority: '1.0' },
    { path: '/courses', changefreq: 'daily', priority: '0.9' },
    { path: '/about', changefreq: 'monthly', priority: '0.5' },
    { path: '/faq', changefreq: 'monthly', priority: '0.5' },
    { path: '/become-instructor', changefreq: 'monthly', priority: '0.5' },
];

/** Routes that must never be crawled: private areas, auth flows and one-off verification links. */
const DISALLOWED = [
    '/admin/',
    '/instructor/',
    '/profile',
    '/my-learning',
    '/achievements',
    '/certificates',
    '/messages',
    '/notifications',
    '/wishlist',
    '/payment/',
    '/login',
    '/register',
    '/forgot-password',
    '/reset-password',
    '/verify-email',
    '/verify/',
];

async function fetchPublishedCourses() {
    if (!API_URL) {
        console.warn('[seo] VITE_API_URL is not set — emitting static routes only.');
        return [];
    }

    const courses = [];
    for (let skip = 0; ; skip += PAGE_SIZE) {
        const response = await fetch(`${API_URL}/courses?skip=${skip}&take=${PAGE_SIZE}`);
        if (!response.ok) throw new Error(`GET /courses returned ${response.status}`);

        const page = await response.json();
        courses.push(...page.items);
        if (courses.length >= page.totalCount || page.items.length === 0) break;
    }
    return courses;
}

function urlEntry({ path: routePath, changefreq, priority, lastmod }) {
    const lines = [
        '  <url>',
        `    <loc>${SITE_URL}${routePath}</loc>`,
        lastmod && `    <lastmod>${lastmod.slice(0, 10)}</lastmod>`,
        `    <changefreq>${changefreq}</changefreq>`,
        `    <priority>${priority}</priority>`,
        '  </url>',
    ];
    return lines.filter(Boolean).join('\n');
}

async function main() {
    let courses = [];
    try {
        courses = await fetchPublishedCourses();
        console.log(`[seo] Fetched ${courses.length} published course(s) from ${API_URL}.`);
    } catch (error) {
        console.warn(`[seo] Could not reach the API (${error.message}) — static routes only.`);
    }

    const courseEntries = courses.map((course) => ({
        path: `/courses/${course.id}`,
        changefreq: 'weekly',
        priority: '0.8',
        lastmod: course.updatedAt,
    }));

    const instructorIds = [...new Set(courses.map((c) => c.instructorId).filter(Boolean))];
    const instructorEntries = instructorIds.map((id) => ({
        path: `/instructors/${id}`,
        changefreq: 'weekly',
        priority: '0.6',
    }));

    const entries = [...STATIC_ROUTES, ...courseEntries, ...instructorEntries];
    const sitemap = [
        '<?xml version="1.0" encoding="UTF-8"?>',
        '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">',
        ...entries.map(urlEntry),
        '</urlset>',
        '',
    ].join('\n');

    const robots = [
        '# Generated by scripts/generate-seo-files.mjs — do not edit by hand.',
        'User-agent: *',
        'Allow: /',
        ...DISALLOWED.map((route) => `Disallow: ${route}`),
        '',
        `Sitemap: ${SITE_URL}/sitemap.xml`,
        '',
    ].join('\n');

    await writeFile(path.join(PUBLIC_DIR, 'sitemap.xml'), sitemap, 'utf8');
    await writeFile(path.join(PUBLIC_DIR, 'robots.txt'), robots, 'utf8');
    console.log(`[seo] Wrote sitemap.xml (${entries.length} URLs) and robots.txt for ${SITE_URL}.`);
}

await main();
